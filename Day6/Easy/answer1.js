//Using For Loop
console.log("Iterating using for loop")
for(let i=0;i<=10;i++){
    console.log(i)
}

//Using While Loop
console.log("Iterating using while loop")
let j=0;
while(j<=10){
    console.log(j)
    j++
}

//Using Do-while Loop
console.log("Iterating using Do-While loop")
let k=0;
do{
    console.log(k)
    k++
}while(k<=10)