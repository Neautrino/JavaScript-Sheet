//Using For Loop
console.log("Iterating using for loop")
for(let i=10;i>=0;i--){
    console.log(i)
}

//Using While Loop
console.log("Iterating using while loop")
let j=10;
while(j>=0){
    console.log(j)
    j--
}

//Using Do-while Loop
console.log("Iterating using Do-While loop")
let k=10;
do{
    console.log(k)
    k--
}while(k>=0)